export function tDashboard(key) {
  return dashboardText[key] || key;
}

export function tDashboardValue(value) {
  if (!value) {
    return value;
  }

  const text = tDashboard(value);
  if (text !== value) {
    return text;
  }

  const triggeredRule = value.match(/^Triggered by stored rule: (.+)$/);
  if (triggeredRule) {
    return `Saklanan kural tetikledi: ${tDashboard(triggeredRule[1])}`;
  }

  const activeLocks = value.match(/^(\d+) active withdrawal lock\(s\)$/);
  if (activeLocks) {
    return `${activeLocks[1]} aktif arınma kaydı`;
  }

  const repeatedTreatments = value.match(
    /^(\d+) animal\(s\) with repeated treatments$/
  );
  if (repeatedTreatments) {
    return `${repeatedTreatments[1]} hayvanda tekrarlanan tedavi`;
  }

  const economicAttention = value.match(
    /^(\d+) animal\(s\) requiring economic attention$/
  );
  if (economicAttention) {
    return `${economicAttention[1]} hayvanda ekonomik dikkat`;
  }

  const mortalityExits = value.match(/^(\d+) mortality exit\(s\)$/);
  if (mortalityExits) {
    return `${mortalityExits[1]} ölüm çıkışı`;
  }

  const averageScore = value.match(/^Average economic score is (.+)$/);
  if (averageScore) {
    return `Ortalama ekonomik skor ${averageScore[1]}`;
  }

  const highestScore = value.match(/^Highest economic score is (.+)$/);
  if (highestScore) {
    return `En yüksek ekonomik skor ${highestScore[1]}`;
  }

  const activeAnimals = value.match(
    /^(\d+) active animal\(s\) available for ranking$/
  );
  if (activeAnimals) {
    return `${activeAnimals[1]} aktif hayvan sıralama için uygun`;
  }

  return value;
}
